package Operators;

public class Bitwise {

	public static void main(String[] args) {
// Bitwise ->| - or , ^ -XOR, ~ 
		
		System.out.println(true & true);
		System.out.println(true | false);
		System.out.println(true ^ false);
		
		System.out.println(7 & 8);
		System.out.println(7 | 8);
		System.out.println(7 ^ 8);
		
		System.out.println( ~8);
		System.out.println(!true);
	}

}
